function actOfValor(action) {
    if (action === 'save a life') return "This is an act of valor!"
    else if (action === 'help the elderly') return "This is an act of valor!"
    else if (action === 'defend the weak') return "This is an act of valor!"
    else return "This action is not considered an act of valor."
}
