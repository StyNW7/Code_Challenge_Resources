function deerObserver(deerName, observerName) {
    if (deerName === 'Bambi' && observerName === 'Thumper'){
        return "Hello, Bambi! Thumper is happy to see you today in the peaceful forest."
    }
    else return "The observer didn\'t spot Bambi today, the forest is still peaceful."
}
